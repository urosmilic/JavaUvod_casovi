public class TipoviPromenljivih {
    public static void main(String[] args) {

        String a; //Niz karakter (Ime, Prezime, Adresa,...)
        int x; //Ceo broj (1,2,3,4,...)
        double y; //Realni broj tj. decimalni broj (2.529476, 1.998324,...), koristi tacka, nikako zarez
        float z; //Realni broj sa manjom memorijom (3.54, 64.98,...)
        float u = 1.2351f; //Na kraju broja obavezno je dodati slovo "f" ili "F"
        long k; //Ceo broj sa vecom memorijom (190257901571095, 1935091509,...)
        long j = 11857815L; //Na kraju broja obavezno je dodati slovo "L"
        char c; //Jedan karakter, koristi se apostrof tj jednostrucni navodnik
        boolean b; //Logicki tip podataka, moze biti "true" ili "false"

        a = "Ime";
        x = 1;
        y = 5.76;
        z = 1.625F;
        k = 78162571L;
        c = 'u';
        b = true;



    }
}